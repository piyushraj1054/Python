# OPERATORS
# Addition => +
# Subtraction => -
# Multiply => *
# Division => /
# Floor Division => // => ignore decimal value
# Exponential => ** => to the power
# Modulus => % => Remainder
a = 10
b = 2
print("Addition of ", a, " + ", b, "is", a+b)
print("Subtraction of ", a, " + ", b, "is", a-b)
print("Multiplication of ", a, " + ", b, "is", a*b)
print("Division of ", a, " + ", b, "is", a/b)
print("Floor division of ", a, " + ", b, "is", a//b)
print("Exponential value of ", a, " + ", b, "is", a**b)
print("Modulus value of ", a, " + ", b, "is", a%b)